def soma(num1, num2):
    soma1 = num1
    soma2 = num2
    soma_total = soma1 + soma2
    print(soma_total)


def sub(num1, num2):
    sub1 = num1
    sub2 = num2
    sub_total = sub1 - sub2
    print(sub_total)


def mult(num1, num2):
    mult1 = num1
    mult2 = num2
    mult_total = mult1 * mult2
    print(mult_total)


def div(num1, num2):
    div1 = num1
    div2 = num2
    div_total = div1 / div2
    print(div_total)


mult(4, 4)
div(20, 4)
sub(1, 3)
soma(2, 2)
