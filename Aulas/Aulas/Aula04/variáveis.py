# Variáveis definidas.
inteira = 30
flutuante = 2.4
verdadeiro_ou_falso = True
texto = "Olá Mundo!"

# Type mostra o tipo da variável no código.
print(type(inteira))
print(type(flutuante))
print(type(verdadeiro_ou_falso))
print(type(texto))

# Print mostra os valores contidos em cada variável.
print(inteira)
print(flutuante)
print(verdadeiro_ou_falso)
print(texto)

